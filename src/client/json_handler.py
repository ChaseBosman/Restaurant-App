import json


class JsonHandler:

    @staticmethod
    def write_json(phone_entry, first_entry, last_entry, number_entry, street_entry, city_entry, state_entry):
        data = {'info': []}
        data['info'].append({
            'first': first_entry,
            'last': last_entry,
            'phone': phone_entry,
            'houseNum': number_entry,
            'street': street_entry,
            'city': city_entry,
            'state': state_entry
        })

        with open('data.txt', 'w') as outfile:
            json.dump(data, outfile)

    @staticmethod
    def read_json():
        data = {}
        with open('data.txt', 'r') as infile:
            data = json.load(infile)
            print("Hello " + str(data['info'][0]['first']) + "!")
